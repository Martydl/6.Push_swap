#include "libft.h"

void	ft_putnbr(int n)
{
	long	nb;

	nb = n;
	(nb < 0 && (nb = -nb)) ? ft_putchar('-') : nb;
	(nb >= 10) ? ft_putnbr(nb / 10) : nb;
	ft_putchar(nb % 10 + '0');
}
