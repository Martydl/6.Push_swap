#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	zero;

	zero = '\0';
	i = 0;
	while (i < n)
	{
		((unsigned char *)s)[i] = zero;
		i++;
	}
}
